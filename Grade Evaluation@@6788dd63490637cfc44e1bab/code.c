#include <stdio.h>


int main() {
    char grades;
    scanf("%c",&grades);

    if(grade=='A'){
        printf("Excellent");
    }
    if(grade=='B'){
        printf("Good");
    }
    if(grade=='C'){
        printf("Average");
    }
    if(grade=='D'){
        printf("Below Average");
    }
    if(grade=='F'){
        printf("Fail");
    }
    
    return 0;
}